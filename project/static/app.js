document.addEventListener('DOMContentLoaded', () => {
    // DOM Elements
    const tabBtns = document.querySelectorAll('.tab-btn');
    const inputForms = document.querySelectorAll('.input-form');
    
    const textForm = document.getElementById('text-form');
    const imageForm = document.getElementById('image-form');
    
    const fileInput = document.getElementById('image-input');
    const fileNameDisplay = document.getElementById('selected-file-name');
    const uploadWrapper = document.querySelector('.file-upload-wrapper');
    
    const resultsDisplay = document.getElementById('results-display');
    const statusBadge = document.getElementById('status-badge');

    // Setup Tabs Handling
    tabBtns.forEach(btn => {
        btn.addEventListener('click', () => {
            // Remove active from all
            tabBtns.forEach(b => b.classList.remove('active'));
            inputForms.forEach(f => f.classList.remove('active'));
            
            // Add active to clicked
            btn.classList.add('active');
            const target = btn.getAttribute('data-target');
            document.getElementById(target === 'text-input' ? 'text-form' : 'image-form').classList.add('active');
        });
    });

    // File Upload Visuals
    fileInput.addEventListener('change', (e) => {
        if (e.target.files.length > 0) {
            fileNameDisplay.textContent = e.target.files[0].name;
            uploadWrapper.style.borderColor = 'var(--success)';
        } else {
            fileNameDisplay.textContent = "No file selected";
            uploadWrapper.style.borderColor = 'var(--glass-border)';
        }
    });

    ['dragover', 'dragleave', 'drop'].forEach(eventName => {
        uploadWrapper.addEventListener(eventName, (e) => e.preventDefault());
    });
    
    uploadWrapper.addEventListener('dragover', () => {
        uploadWrapper.classList.add('dragover');
    });
    
    uploadWrapper.addEventListener('dragleave', () => {
        uploadWrapper.classList.remove('dragover');
    });
    
    uploadWrapper.addEventListener('drop', (e) => {
        uploadWrapper.classList.remove('dragover');
        if (e.dataTransfer.files.length) {
            fileInput.files = e.dataTransfer.files;
            fileNameDisplay.textContent = e.dataTransfer.files[0].name;
            uploadWrapper.style.borderColor = 'var(--success)';
        }
    });

    // Get selected module utility
    const getSelectedModule = () => document.querySelector('input[name="module"]:checked').value;

    // Loading State handlers
    const setLoading = (buttonEl, isLoading) => {
        if (isLoading) {
            buttonEl.classList.add('loading');
            buttonEl.disabled = true;
            statusBadge.textContent = "Analyzing...";
            statusBadge.className = "status-badge";
            resultsDisplay.innerHTML = `
                <div class="empty-state">
                    <span class="empty-icon">⏳</span>
                    <p>Processing with AI model...</p>
                </div>
            `;
        } else {
            buttonEl.classList.remove('loading');
            buttonEl.disabled = false;
        }
    };

    const renderResults = (data) => {
        let html = '';
        
        // Show Extracted Text if Image
        if (data.input_type === 'image' && data.extracted_text) {
            html += `
                <div class="info-box extracted">
                    <h4>OCR Extracted Text</h4>
                    <p>"${data.extracted_text}"</p>
                </div>
            `;
        }

        // Show Disclaimer if Drug
        if (data.module === 'drug') {
            html += `
                <div class="info-box">
                    <h4>Medical Disclaimer</h4>
                    <p>The information provided is generated by AI. Please consult a medical professional before use.</p>
                </div>
            `;
        }

        // Check for error inside data
        if (data.data && data.data.error) {
             html += `
                <div class="info-box" style="border-left-color: var(--danger); background: rgba(239, 68, 68, 0.1);">
                    <h4 style="color:var(--danger)">Error</h4>
                    <p>${data.data.error}</p>
                </div>
            `;
            resultsDisplay.innerHTML = html;
            return;
        }

        // Render JSON keys dynamically
        const analysisData = data.data;
        if (analysisData && Object.keys(analysisData).length > 0) {
            for (const [key, value] of Object.entries(analysisData)) {
                const displayKey = key.replace(/_/g, ' ');
                
                html += `<div class="result-card">
                    <span class="result-key">${displayKey}</span>`;
                    
                if (Array.isArray(value)) {
                    html += `<ul class="result-list">`;
                    value.forEach(item => {
                        html += `<li>${item}</li>`;
                    });
                    html += `</ul>`;
                } else {
                    html += `<div class="result-value">${value}</div>`;
                }
                html += `</div>`;
            }
        } else {
             html += `
                <div class="empty-state">
                    <span class="empty-icon">⚠️</span>
                    <p>No valid data returned from the model.</p>
                </div>
            `;
        }

        resultsDisplay.innerHTML = html;
        statusBadge.textContent = "Analysis Complete";
        statusBadge.className = "status-badge success";
    };

    const handleError = (error) => {
        resultsDisplay.innerHTML = `
            <div class="empty-state">
                <span class="empty-icon" style="filter:none">❌</span>
                <p style="color:var(--danger)">An error occurred: ${error.message}</p>
            </div>
        `;
        statusBadge.textContent = "Analysis Failed";
        statusBadge.className = "status-badge error";
    };

    // Text Form Submit
    textForm.addEventListener('submit', async (e) => {
        e.preventDefault();
        const text = document.getElementById('text-input').value;
        const btn = document.getElementById('analyze-text-btn');
        const module = getSelectedModule();

        setLoading(btn, true);

        try {
            const formData = new FormData();
            formData.append('module', module);
            formData.append('text', text);

            const response = await fetch('/analyze/text', {
                method: 'POST',
                body: formData
            });

            if(!response.ok) throw new Error(`HTTP Error: ${response.status}`);
            const data = await response.json();
            renderResults(data);
            
        } catch (err) {
            handleError(err);
        } finally {
            setLoading(btn, false);
        }
    });

    // Image Form Submit
    imageForm.addEventListener('submit', async (e) => {
        e.preventDefault();
        const btn = document.getElementById('analyze-image-btn');
        const module = getSelectedModule();
        
        if (!fileInput.files.length) {
            alert("Please select an image file first.");
            return;
        }

        setLoading(btn, true);

        try {
            const formData = new FormData();
            formData.append('module', module);
            formData.append('file', fileInput.files[0]);

            const response = await fetch('/analyze/image', {
                method: 'POST',
                body: formData
            });

            if(!response.ok) throw new Error(`HTTP Error: ${response.status}`);
            const data = await response.json();
            renderResults(data);
            
        } catch (err) {
            handleError(err);
        } finally {
            setLoading(btn, false);
        }
    });

});
